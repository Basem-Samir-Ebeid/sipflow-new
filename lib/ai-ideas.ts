function buildIdeas(): AiIdea[] {
  const ideas: AiIdea[] = []
  const seenTitles = new Set<string>()
  const seenKeys = new Set<string>()

  for (const cat of CATEGORIES) {
    cat.bases.forEach((base, baseIdx) => {
      MODIFIERS.forEach(mod => {
        const title = base.title + mod.suffix
        if (seenTitles.has(title)) return
        const lookup = `${cat.prefix}:${baseIdx}:${mod.key}`
        const flagKey = PRESERVED_KEYS[lookup] || `idea_${cat.prefix}_${baseIdx}_${mod.key}`
        if (seenKeys.has(flagKey)) return
        seenTitles.add(title)
        seenKeys.add(flagKey)
        ideas.push({
          title,
          desc: base.desc.replace(/\.?$/, '') + mod.descSuffix,
          icon: cat.icon,
          color: cat.color,
          tab: cat.tab,
          tabLabel: cat.tabLabel,
          flagKey,
        })
      })
    })
  }
  return ideas
}

export const AI_IDEAS: AiIdea[] = buildIdeas()
export const AI_IDEAS_COUNT: number = AI_IDEAS.length
